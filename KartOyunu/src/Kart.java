public class Kart {
   
   private char deger;
   private boolean tahmin_et = false;

    public Kart(char deger) {
        this.deger = deger;
    }

    public char getDeger() {
        return deger;
    }

    public void setDeger(char deger) {
        this.deger = deger;
    }

    public boolean isTahmin_et() {
        return tahmin_et;
    }

    public void setTahmin_et(boolean tahmin_et) {
        this.tahmin_et = tahmin_et;
    }
   
   
   
   
}
