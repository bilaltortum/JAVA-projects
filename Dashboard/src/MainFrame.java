    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
       DefaultTableModel model = (DefaultTableModel)tablo.getModel();
        int row = tablo.getSelectedRow();
        if (row<0){
            JOptionPane.showMessageDialog(this, "Bir alan seçiniz...",
            "Tekrar deneyiniz", JOptionPane.ERROR_MESSAGE);
        }
        else{
            model.removeRow(row);
        }
    }//GEN-LAST:event_silActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
// tablodaki verileri kaydetmek için  JFrame(sağ tık) > Event > window > windowclosing seç ve şu kodları yaz
    
    DefaultTableModel model = (DefaultTableModel)tablo.getModel();
    Vector<Vector> tabloData = model.getDataVector();
    
    try{
        FileOutputStream dosya = new FileOutputStream("dosya.bin");
        ObjectOutputStream output = new ObjectOutputStream(dosya);
        
        output.writeObject(tabloData);
        output.close();
        dosya.close();
    }
    catch(Exception ex){
        ex.printStackTrace();
    }
    
    }//GEN-LAST:event_formWindowClosing

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
// programı açtığımızda kaydedilen verileri okumak için JFrame(sağ tık) > Event > window > windowopening seç ve şu kodları
    
    try{
        FileInputStream dosya = new FileInputStream("dosya.bin");
        ObjectInputStream input = new ObjectInputStream(dosya);
        
       Vector<Vector> tableData = (Vector<Vector>)input.readObject();

        
        input.close();
        dosya.close();
        
        DefaultTableModel model = (DefaultTableModel)tablo.getModel();
       for(int i=0; i< tableData.size(); i++){
           Vector row = tableData.get(i);
           model.addRow(new Object[]{row.get(0),row.get(1),row.get(2),row.get(3)});
       }
    }catch(Exception ex){
        ex.printStackTrace();
    }

    }//GEN-LAST:event_formWindowOpened

