    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
        String isim = ad.getText();
        String email = posta.getText();
        String tlfn = telefon.getText();
        String adrs = adres.getText();

        DefaultTableModel model = (DefaultTableModel)tablo.getModel();  // eksik alan olmazsa girilen değerler tabloya eklenir

        if (isim.isEmpty() || email.isEmpty() || tlfn.isEmpty() || adrs.isEmpty()){
            JOptionPane.showMessageDialog(this, "Boş alan bırakılamaz",
            "Tekrar deneyiniz", JOptionPane.ERROR_MESSAGE);  // ekleme sırasında boş alan bırakıldığında verilecek error
        }
        else{
            Object[] eklenecek  = {ad.getText(),posta.getText(),telefon.getText(),adres.getText()};
            model.addRow(eklenecek);
            ad.setText("");
            posta.setText("");
            telefon.setText("");
            adres.setText("");            
        }
    }//GEN-LAST:event_ekleActionPerformed

